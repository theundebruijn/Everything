/// NPM ///
import { getFeatures } from 'detect-features';


///////////////
///// OBJ /////
///////////////

const ENV = Object.create(null);


///////////////////////
///// OBJ METHODS /////
///////////////////////

ENV.detectFeatures = async function (fCB) {
  this.features = await getFeatures();
  fCB();
};

ENV.getFeatures = function() {
  return this.features;
};


//////////////////////
///// ES6 EXPORT /////
//////////////////////

export { ENV };


